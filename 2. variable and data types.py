# Python Literals can be defined as data that is given in a variable or constant.

# Four types of literals: 
# 1) String literals, 
# 2) Numeric literals, 
# 3) Boolean literals and 
# 4) a special literal None.


# 1. String literals:
# String literals can be formed by enclosing a text in the quotes. We can use both single as well as double quotes to create a string.

# Two types of Strings supported in Python
# a) Single-Line String

name = 'Ramesh'

print(name)

name="Ramesh"
print(name)

# b) Multi-line String
#      There are two ways to create multiline strings:
# 1) Adding black slash at the end of each line.

text1='Hello\
User'
print(text1)

# 2) Using single triple quotation marks:-

text2='''welcome
to
Internship'''

print(text2)

   
# 2. Numeric literals:
# Numeric Literals are immutable. Numeric literals can belong to following four different numerical types.

# Int(signed integers)
# Long(long integers)
# float(floating point)
# Complex(complex)

a = 10.5
b = 10

print(a)
print(b)

# 3. Boolean literals:

# A Boolean literal can have any of the two values: True or False.
# Example:

a = 5
b = 10

print(a > b)
print(b > a)

# 4. Special literals: None
# Python contains one special literal i.e., None.
# None is used to specify to that field that is "not created". 
# It is also used for the end of lists in Python.
